/*  sequence方法 进行排序和获取name、value
*   completeArray   全部数组对象
*   nameComplete    全部name名称
*   valueComplete   全部的vlaue
*   maxArray        6个最大值
*   nameMax         name名称
*   valueMax        value值
*/
function sequence(sourceData){
    var nameComplete = [],valueComplete = [],nameMax = [],valueMax = [];
    var completeArray = sourceData.sort(function(a,b){
        return b.value - a.value;
    });
    for(var i = 0; i < completeArray.length; i++){
        var temp = completeArray[i];
        nameComplete.push(temp.name);
        valueComplete.push(temp.value);
    }
    var maxArray = completeArray.slice(0,6);
    for(var i = 0; i < maxArray.length; i++){
        var temp = maxArray[i];
        nameMax.push(temp.name);
        valueMax.push(temp.value);
    }
    return [completeArray,nameComplete,valueComplete,maxArray,nameMax,valueMax]
}


